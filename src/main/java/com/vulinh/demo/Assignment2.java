package com.vulinh.demo;

public class Assignment2 {

    public static void main(String[] args) {
        int value = 12;

        System.out.println(value);

        value += 10; // -> value = value + 10

        System.out.println(value);

        value -= 5; // -> value = value - 5

        System.out.println(value);

        value /= 4; // -> value = value / 4

        System.out.println(value);

        value *= 13; // -> value = value * 13

        System.out.println(value);

        value %= 2;

        System.out.println(value);
    }
}
