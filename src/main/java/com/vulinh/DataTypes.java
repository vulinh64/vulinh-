package com.vulinh;

public class DataTypes {

    public static void main(String[] args) {
        // Số nguyên 54
        int integer = 54;

        // Số nguyên 5879 (max min lớn hơn integer)
        long longNumber = 5879L;

        // Số thực kiểu float
        float floatNumber = 4.68F;

        // Số thực kiểu double
        double doubleNumber = 1.54;

        boolean booleanValue = true;
        boolean newBooleanValue = false;

        // Ký tự
        char character = 'A';

        char vn = 'á';

        short shortValue = 14;
        byte byteValue = 120;

        // byte a = 128; compile error

        String name = "Vũ Linh";

        // define and initialize
        int notInitialized;

        notInitialized = 56;

        System.out.println(notInitialized);
    }
}
